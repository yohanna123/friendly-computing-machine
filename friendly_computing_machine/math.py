"""
A small set of functions for doing math operations.
"""

# Write a function named add that adds two values. 

def add(value1, value2):
    """
    Fun
    """
    answer = value1 + value2
    return answer

def mult(arg1,arg2):

    """
    multipleis
    """
    return arg1 * arg2

def divide(arg1,arg2):

    """
    divides
    """
    return arg1 / arg2

def is_greater_than(arg1, arg2):

    """
    checks if arg1 is greater than arg2, return truth

    """
    if arg1 > arg2:
        return True

    if arg1 < arg2:
        return False 
     
